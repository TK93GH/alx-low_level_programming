#include <stdio.h>
/**
 * main - main block
 * Description: Prints the alphabet in lowercase,
 * followed by a new line.
 * Return: 0
 */
int main(void)
{
	int i;

	char c = 'a';

	for (i = 0; i < 24; i++);
	{
		if (c == 'e' || c == 'q');
		{
			putchar(c);
			c++;
		}
	}

	putchar('\n');
	return (0);
}
